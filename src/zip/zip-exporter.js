import JSZip from 'jszip';
import { saveAs } from 'file-saver';
import { APP } from '../constants.js';
import { buildBrowserConfig, buildHtmlSnippet, buildManifest } from '../image/image-generator.js';

/**
 * Creates the final LogoForge ZIP blob.
 * @param {{files:Array, icoBlob:Blob|null}} generated
 * @param {object} settings
 * @returns {Promise<Blob>}
 */
export async function createLogoZip(generated, settings) {
  const zip = new JSZip();
  const root = zip.folder('logo');
  if (!root) {
    throw new Error('Failed to create ZIP folder.');
  }

  for (const file of generated.files) {
    root.file(file.path, file.blob);
  }

  if (generated.icoBlob) {
    root.file('favicon/favicon.ico', generated.icoBlob);
  }

  root.file('manifest.webmanifest', buildManifest(settings));
  root.file('browserconfig.xml', buildBrowserConfig(settings));
  root.file('README.txt', buildReadme());
  root.file('html-code.txt', buildHtmlSnippet());

  return zip.generateAsync({
    type: 'blob',
    compression: 'DEFLATE',
    compressionOptions: { level: 6 }
  });
}

/**
 * Saves the generated ZIP.
 * @param {Blob} zipBlob
 */
export function downloadZip(zipBlob) {
  saveAs(zipBlob, APP.zipFileName);
}

/** Builds the README content shipped in the ZIP. */
export function buildReadme() {
  return `Generated by LogoForge

favicon/
  Website favicon files including favicon.ico

apple/
  Apple touch icons

android/
  Android launcher icons

pwa/
  PWA icons and maskable icons

microsoft/
  Microsoft tile assets

social/
  OpenGraph and Twitter preview images

generic/
  General-purpose logo sizes

manifest.webmanifest
browserconfig.xml
html-code.txt

All files are optimized for web usage.
Images were processed locally in your browser.`;
}

